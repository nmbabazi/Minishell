#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	t_list *iterator;
	size_t len;

	len = 0;
	iterator = lst;
	while (iterator)
	{
		iterator = iterator->next;
		len++;
	}
	return (len);
}
